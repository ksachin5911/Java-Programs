package com.company;

public class a25Escape_Sequence {
    public static void main(String[] args){
        System.out.println("My name is \"Sachin\"");/*to print "" we use a 
                                                     backslash with "" */
        //similarly to print a backslash we use it with a backslash or \b
        System.out.println("I am a \\person");
        //to print a new line
        System.out.println("Hello \n I am Sachin");
        //to print a tab space
        System.out.println("three girls: shreya \t tina \t tania" );
        System.out.println("Hey pls \f give me ur number");// it prints a form feed
    }
}
