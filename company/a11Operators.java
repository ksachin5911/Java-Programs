package com.company;

public class a11Operators {
    public static void main(String[] args){
        int a = 3; //Arithmetic Operators 
        int b = 9; //Arithmetic Operators 
        int sum = a+b; //Arithmetic Operators
        int mul = a*b; //Arithmetic Operators
        int sub = a-b; //Arithmetic Operators
        int mod = b%a; //Arithmetic Operators
        int div = b/a; //Arithmetic Operators
        System.out.println(sum);
        System.out.println(mul);
        System.out.println(sub);
        System.out.println(mod);
        System.out.println(div);
        System.out.println(++a);
        System.out.println(--b);
        a += 56; // Assignment operator
        System.out.println(a);
        System.out.println(45>34); // Comparison operator
        System.out.println(37<12);
        System.out.println(23>12 && 23<9); // Logical operator
        System.out.println(6&5);
        System.out.println(9|3); 
    }
}
