package com.company;

public class a19Substring {
    public static void main(String[] args){
        String name = "School";
        System.out.println(name.length());
        System.out.println(name.substring(2));/*name.substring(n) where n
                                                         is the begin index*/
        System.out.println(name.substring(1, 6));/*here the 
                                                        end index is excluded */
    }
}
